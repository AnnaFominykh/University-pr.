package com.company;

public class Group {
    private int gr_id;
    private String gr_name;
    private String gr_marks;


    public int getGr_id() {
        return gr_id;
    }

    public void setGr_id(int gr_id) {
        this.gr_id = gr_id;
    }

    public String getGr_name() {
        return gr_name;
    }

    public void setGr_name(String gr_name) {
        this.gr_name = gr_name;
    }

    public String getGr_marks() {
        return gr_marks;
    }

    public void setGr_marks(String gr_marks) {
        this.gr_marks = gr_marks;
    }
}
